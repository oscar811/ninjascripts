namespace NinjaTrader.NinjaScript.Strategies
{
	public class TimeTestStrat : Strategy
	{
		private SessionHighLow SessionHighLow1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "TimeTestStrat";
				Calculate									= Calculate.OnBarClose;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				Start_time						= DateTime.Parse("03:22", System.Globalization.CultureInfo.InvariantCulture);
				End_time						= DateTime.Parse("04:22", System.Globalization.CultureInfo.InvariantCulture);
			}
			else if (State == State.Configure)
			{
			}
			else if (State == State.DataLoaded)
			{				
				SessionHighLow1				= SessionHighLow(Close, DateTime.Parse("1:30 AM"), DateTime.Parse("3:00 AM"));
				SessionHighLow1.Plots[0].Brush = Brushes.Transparent;
				SessionHighLow1.Plots[1].Brush = Brushes.Transparent;
				AddChartIndicator(SessionHighLow1);
				SetProfitTarget("", CalculationMode.Currency, 500);
				SetStopLoss(CalculationMode.Currency, 200);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;

			 // Set 1
			if ((Times[0][0].TimeOfDay >= Start_time.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= End_time.TimeOfDay)
				 && (Close[0] <= SessionHighLow1.Session_High[0]))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), "");
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[PropertyEditor("NinjaTrader.Gui.Tools.TimeEditorKey")]
		[Display(Name="Start_time", Description="start_time", Order=1, GroupName="Parameters")]
		public DateTime Start_time
		{ get; set; }

		[NinjaScriptProperty]
		[PropertyEditor("NinjaTrader.Gui.Tools.TimeEditorKey")]
		[Display(Name="End_time", Description="End_time", Order=2, GroupName="Parameters")]
		public DateTime End_time
		{ get; set; }
		#endregion

	}
}

