		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;

			 // Set 1
			if ((Close[0] > CurrentDayOHL1.CurrentOpen[0])
				 && (Times[0][0].TimeOfDay >= Start_1.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_1.TimeOfDay)
				 && (Additional_Time_1 == true)
				 && (CrossAbove(High, Bollinger1.Upper, 1))
				 // Condition group 1
				 && ((Times[0][0].DayOfWeek == DayOfWeek.Monday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Tuesday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Sunday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Friday)))
			{
				EnterShort(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 2
			if ((Times[0][0].TimeOfDay >= Start_1.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_1.TimeOfDay)
				 && (Close[0] < CurrentDayOHL2.CurrentOpen[0])
				 && (Additional_Time_1 == true)
				 && (CrossBelow(Low, Bollinger1.Lower, 1))
				 // Condition group 1
				 && ((Times[0][0].DayOfWeek == DayOfWeek.Monday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Tuesday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Sunday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Friday)))
			{
				EnterLong(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 3
			if ((Close[0] > CurrentDayOHL1.CurrentOpen[0])
				 && (Times[0][0].TimeOfDay >= Start_2.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_2.TimeOfDay)
				 && (Additional_Time_2 == true)
				 && (CrossAbove(High, Bollinger1.Upper, 1)))
			{
				EnterShort(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 4
			if ((Times[0][0].TimeOfDay >= Start_2.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_2.TimeOfDay)
				 && (Close[0] < CurrentDayOHL2.CurrentOpen[0])
				 && (Additional_Time_2 == true)
				 && (CrossBelow(Low, Bollinger1.Lower, 1)))
			{
				EnterLong(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 5
			if ((Close[0] > CurrentDayOHL1.CurrentOpen[0])
				 && (Additional_Time_3 == true)
				 && (Times[0][0].TimeOfDay > Start_3.TimeOfDay)
				 && (Times[0][0].TimeOfDay < Stop_3.TimeOfDay)
				 && (CrossAbove(High, Bollinger1.Upper, 1))
				 // Condition group 1
				 && ((Times[0][0].DayOfWeek == DayOfWeek.Monday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Tuesday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Thursday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Friday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Sunday)))
			{
				EnterShort(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 6
			if ((Close[0] < CurrentDayOHL2.CurrentOpen[0])
				 && (Additional_Time_3 == true)
				 && (Times[0][0].TimeOfDay > Start_3.TimeOfDay)
				 && (Times[0][0].TimeOfDay < Stop_3.TimeOfDay)
				 && (CrossBelow(Low, Bollinger1.Lower, 1))
				 // Condition group 1
				 && ((Times[0][0].DayOfWeek == DayOfWeek.Monday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Tuesday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Thursday)
				 || (Times[0][0].DayOfWeek == DayOfWeek.Friday)))
			{
				EnterLong(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 7
			if ((Close[0] > CurrentDayOHL1.CurrentOpen[0])
				 && (Times[0][0].TimeOfDay >= Start_Time_4.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_Time_4.TimeOfDay)
				 && (Additional_TIme_4 == true)
				 && (CrossAbove(High, Bollinger1.Upper, 1)))
			{
				EnterShort(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 8
			if ((Times[0][0].TimeOfDay >= Start_Time_4.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_Time_4.TimeOfDay)
				 && (Close[0] < CurrentDayOHL2.CurrentOpen[0])
				 && (Additional_TIme_4 == true)
				 && (CrossBelow(Low, Bollinger1.Lower, 1)))
			{
				EnterLong(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 9
			if ((Close[0] > CurrentDayOHL1.CurrentOpen[0])
				 && (Times[0][0].TimeOfDay >= Start_5.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_5.TimeOfDay)
				 && (Additional_Time_5 == true)
				 && (CrossAbove(High, Bollinger1.Upper, 1)))
			{
				EnterShort(Convert.ToInt32(CONTRACTS), "");
			}
			
			 // Set 10
			if ((Times[0][0].TimeOfDay >= Start_5.TimeOfDay)
				 && (Times[0][0].TimeOfDay <= Stop_5.TimeOfDay)
				 && (Close[0] < CurrentDayOHL2.CurrentOpen[0])
				 && (Additional_Time_5 == true)
				 && (CrossBelow(Low, Bollinger1.Lower, 1)))
			{
				EnterLong(Convert.ToInt32(CONTRACTS), "");
			}
			
		}

